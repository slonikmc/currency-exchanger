package ru.bgtu.currencyexchanger.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.http.converter.HttpMessageConverter;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

import java.util.List;

@Configuration
public class WebConfig implements WebMvcConfigurer {

    private final HtmlJsonMessageConverter htmlJsonMessageConverter;

    public WebConfig(HtmlJsonMessageConverter htmlJsonMessageConverter) {
        this.htmlJsonMessageConverter = htmlJsonMessageConverter;
    }

    @Override
    public void configureMessageConverters(List<HttpMessageConverter<?>> converters) {
        converters.add(new MappingJackson2HttpMessageConverter());
        converters.add(htmlJsonMessageConverter);
    }
}